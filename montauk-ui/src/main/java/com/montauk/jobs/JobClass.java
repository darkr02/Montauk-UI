package com.montauk.jobs;

public class JobClass {
	
	private String jobName;
	
	private String completionPercent;
	
	private String jobRefId;

	private String jobType;

	private String jobStatus;

	private String jobDescription;
	
	private String jobDuration;

	
	public String getJobName() {
		return jobName;
	}

	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	
	public String getJobRefId() {
		return jobRefId;
	}

	public void setJobRefId(String jobRefId) {
		this.jobRefId = jobRefId;
	}

	
	public String getjobType() {
		return jobType;
	}

	public void setjobType(String jobType) {
		this.jobType = jobType;
	}

	
	
	public String getjobStatus() {
		return jobStatus;
	}

	public void setjobStatus(String jobStatus) {
		this.jobStatus = jobStatus;
	}

	
	
	public String getjobDescription() {
		return jobDescription;
	}

	public void setjobDescription(String jobDescription) {
		this.jobDescription = jobDescription;
	}

	
	public String getCompletionPercent() {
		return completionPercent;
	}

	public void setCompletionPercent(String completionPercent) {
		this.completionPercent = completionPercent;
	}
	
	public String getjobDuration() {
		return jobDuration;
	}

	public void setjobDuration(String jobDuration) {
		this.jobDuration = jobDuration;
	}


	
}



